    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        RequestDispatcher dispatcher = request.getRequestDispatcher("/user/add.jsp");
        dispatcher.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();
        try 
        {
            Class.forName("com.mysql.jdbc.Driver"); // Load driver
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/n2cms", "root", ""); // Create connection
            if(request.getParameter("btn_add")!=null) // Check button click event not null
            {
                String name, email;
                name=request.getParameter("txt_name"); // txt_name
                email=request.getParameter("txt_email"); // txt_name
                PreparedStatement pstmt = null; // Create statement
                pstmt=con.prepareStatement("insert into users(name, email) values(?,?)"); // SQL insert query
                pstmt.setString(1, name);
                pstmt.setString(2, email);
                pstmt.executeUpdate(); // Execute query
                con.close(); // Close connection
                out.println("Insert Successfully...! <a href='../users/list'>Click back link</a>."); // After insert record successfully message
            }
        }
        catch(Exception e) 
        {
            out.println(e);
        }
    }

